<?php

namespace App\Services;

use App\Models\Production\Production;
use App\Models\Supply\SupplierOrderItem;
use App\Models\Supply\SuppliesMovement;
use App\Models\Supply\Supply;
use App\Models\User;
use Illuminate\Support\Facades\DB;

/**
 * Handles stock entries and exits while keeping traceable movement logs.
 */
class InventoryMovementService
{
    /**
     * Moves a supplier order item into stock exactly once and records inbound movement.
     */
    public function receiveOrderItemIntoStock(SupplierOrderItem $item, string $orderRef, string $deliveryDate, ?User $user = null): Supply
    {
        return DB::transaction(function () use ($item, $orderRef, $deliveryDate, $user): Supply {
            /** @var SupplierOrderItem $lockedItem */
            $lockedItem = SupplierOrderItem::query()
                ->lockForUpdate()
                ->findOrFail($item->id);

            if ($lockedItem->isInSupplies() || $lockedItem->supply()->exists()) {
                throw new \RuntimeException('Supplier order item is already in stock.');
            }

            $totalQuantity = $this->resolveOrderItemStockQuantity($lockedItem);

            if ($totalQuantity <= 0) {
                throw new \InvalidArgumentException('Supplier order item quantity must be greater than zero.');
            }

            $batchNumber = $this->generateUniqueBatchNumber($lockedItem->batch_number);

            $supply = Supply::query()->create([
                'supplier_order_item_id' => $lockedItem->id,
                'supplier_listing_id' => $lockedItem->supplier_listing_id,
                'order_ref' => $orderRef,
                'batch_number' => $batchNumber,
                'unit_price' => $lockedItem->unit_price,
                'initial_quantity' => $totalQuantity,
                'quantity_in' => $totalQuantity,
                'quantity_out' => 0,
                'expiry_date' => $lockedItem->expiry_date,
                'delivery_date' => $deliveryDate,
                'is_in_stock' => true,
            ]);

            $this->recordInboundFromOrderItem($supply, $lockedItem, $user);

            $lockedItem->update([
                'is_in_supplies' => 'Stock',
                'moved_to_stock_at' => now(),
                'moved_to_stock_by' => $user?->id,
            ]);

            $lockedItem->supplierListing()->update([
                'price' => $lockedItem->unit_price,
            ]);

            $lockedItem->loadMissing('supplierListing.ingredient');

            $ingredient = $lockedItem->supplierListing?->ingredient;

            if ($ingredient && $lockedItem->unit_price !== null) {
                $ingredient->update([
                    'price' => $lockedItem->unit_price,
                ]);
            }

            return $supply;
        });
    }

    /**
     * Records an inbound movement generated by supplier reception.
     */
    public function recordInboundFromOrderItem(Supply $supply, SupplierOrderItem $item, ?User $user = null): SuppliesMovement
    {
        $quantity = $this->resolveOrderItemStockQuantity($item);
        $unit = $item->supplierListing?->unit_of_measure ?: 'kg';

        return SuppliesMovement::query()->create([
            'supply_id' => $supply->id,
            'supplier_order_item_id' => $item->id,
            'production_id' => null,
            'user_id' => $user?->id,
            'movement_type' => 'in',
            'quantity' => $quantity,
            'unit' => $unit,
            'reason' => 'Supplier order received into stock',
            'meta' => [
                'supplier_order_id' => $item->supplier_order_id,
                'order_ref' => $supply->order_ref,
                'batch_number' => $supply->batch_number,
            ],
            'moved_at' => now(),
        ]);
    }

    /**
     * Records an inbound movement generated by finished internal production.
     */
    public function recordInboundFromProduction(Supply $supply, Production $production, float $quantityKg, ?User $user = null, ?string $reason = null): SuppliesMovement
    {
        return SuppliesMovement::query()->create([
            'supply_id' => $supply->id,
            'supplier_order_item_id' => null,
            'production_id' => $production->id,
            'user_id' => $user?->id,
            'movement_type' => 'in',
            'quantity' => $quantityKg,
            'unit' => 'kg',
            'reason' => $reason ?? 'Production output moved to stock',
            'meta' => [
                'production_batch' => $production->getLotIdentifier(),
                'supply_batch' => $supply->batch_number,
                'source' => 'production_output',
            ],
            'moved_at' => now(),
        ]);
    }

    /**
     * Records an outbound movement when stock is consumed by production.
     */
    public function recordOutboundToProduction(Supply $supply, Production $production, float $quantityKg, ?User $user = null, ?string $reason = null): SuppliesMovement
    {
        return SuppliesMovement::query()->create([
            'supply_id' => $supply->id,
            'supplier_order_item_id' => $supply->supplier_order_item_id,
            'production_id' => $production->id,
            'user_id' => $user?->id,
            'movement_type' => 'out',
            'quantity' => $quantityKg,
            'unit' => $supply->supplierListing?->unit_of_measure ?: 'kg',
            'reason' => $reason ?? 'Allocated to production',
            'meta' => [
                'production_batch' => $production->getLotIdentifier(),
                'supply_batch' => $supply->batch_number,
            ],
            'moved_at' => now(),
        ]);
    }

    /**
     * Records a manual stock correction movement.
     */
    public function recordAdjustment(Supply $supply, float $quantityKg, string $reason, ?User $user = null): SuppliesMovement
    {
        return SuppliesMovement::query()->create([
            'supply_id' => $supply->id,
            'supplier_order_item_id' => $supply->supplier_order_item_id,
            'production_id' => null,
            'user_id' => $user?->id,
            'movement_type' => 'adjustment',
            'quantity' => $quantityKg,
            'unit' => $supply->supplierListing?->unit_of_measure ?: 'kg',
            'reason' => $reason,
            'meta' => [
                'supply_batch' => $supply->batch_number,
            ],
            'moved_at' => now(),
        ]);
    }

    /**
     * Records an allocation movement when stock is reserved for production.
     *
     * This creates an audit trail for reservations made via ProductionAllocationService.
     */
    public function recordAllocation(Supply $supply, Production $production, float $quantityKg, ?string $reason = null): SuppliesMovement
    {
        return SuppliesMovement::query()->create([
            'supply_id' => $supply->id,
            'supplier_order_item_id' => $supply->supplier_order_item_id,
            'production_id' => $production->id,
            'user_id' => null,
            'movement_type' => 'allocation',
            'quantity' => $quantityKg,
            'unit' => $supply->supplierListing?->unit_of_measure ?: 'kg',
            'reason' => $reason ?? 'Reserved for production',
            'meta' => [
                'production_batch' => $production->getLotIdentifier(),
                'supply_batch' => $supply->batch_number,
            ],
            'moved_at' => now(),
        ]);
    }

    /**
     * Records a release movement (negative allocation) when stock reservation is cancelled.
     *
     * This creates a compensating transaction instead of deleting the original allocation.
     */
    public function recordRelease(Supply $supply, Production $production, float $quantityKg, ?string $reason = null): SuppliesMovement
    {
        // Eager load supplierListing to prevent lazy loading violation
        $supply->loadMissing('supplierListing');

        return SuppliesMovement::query()->create([
            'supply_id' => $supply->id,
            'supplier_order_item_id' => $supply->supplier_order_item_id,
            'production_id' => $production->id,
            'user_id' => null,
            'movement_type' => 'allocation',
            'quantity' => -$quantityKg,
            'unit' => $supply->supplierListing?->unit_of_measure ?: 'kg',
            'reason' => $reason ?? 'Released from production',
            'meta' => [
                'production_batch' => $production->getLotIdentifier(),
                'supply_batch' => $supply->batch_number,
            ],
            'moved_at' => now(),
        ]);
    }

    /**
     * @deprecated Use recordRelease() instead to create compensating transactions
     */
    public function deleteAllocationsForProductionItem(Production $production, Supply $supply, float $quantity): void
    {
        // Create compensating negative movement instead of deleting
        $this->recordRelease($supply, $production, $quantity);
    }

    /**
     * Deletes all allocation movements for a production.
     *
     * Used when cancelling productions.
     */
    public function deleteAllAllocationsForProduction(Production $production): void
    {
        SuppliesMovement::query()
            ->where('production_id', $production->id)
            ->where('movement_type', 'allocation')
            ->delete();
    }

    private function resolveOrderItemStockQuantity(SupplierOrderItem $item): float
    {
        $quantity = (float) ($item->quantity ?? 0);
        $unitWeight = (float) ($item->unit_weight ?? 0);
        $unitMultiplier = $unitWeight > 0 ? $unitWeight : 1;

        return round($quantity * $unitMultiplier, 3);
    }

    /**
     * Generate a unique batch number by appending a sequential suffix if the original already exists.
     *
     * For example, if "ABC123" exists, returns "ABC123-1". If "ABC123-1" also exists, returns "ABC123-2", etc.
     */
    private function generateUniqueBatchNumber(?string $originalBatchNumber): string
    {
        $normalizedBatchNumber = $this->normalizeBatchNumber($originalBatchNumber);

        if ($normalizedBatchNumber === null) {
            return 'NO-BATCH-'.now()->format('YmdHis');
        }

        $lowerBaseBatchNumber = mb_strtolower($normalizedBatchNumber);

        $existingBatchNumbers = Supply::query()
            ->whereNotNull('batch_number')
            ->where(function ($query) use ($lowerBaseBatchNumber): void {
                $query
                    ->whereRaw('LOWER(batch_number) = ?', [$lowerBaseBatchNumber])
                    ->orWhereRaw('LOWER(batch_number) LIKE ?', [$lowerBaseBatchNumber.'-%']);
            })
            ->pluck('batch_number')
            ->filter(fn (?string $batchNumber): bool => filled($batchNumber))
            ->map(fn (string $batchNumber): string => mb_strtoupper(trim($batchNumber)))
            ->values();

        if ($existingBatchNumbers->isEmpty()) {
            return $normalizedBatchNumber;
        }

        $escapedBase = preg_quote($normalizedBatchNumber, '/');
        $maxSuffix = 0;
        $baseAlreadyExists = false;

        foreach ($existingBatchNumbers as $existingBatchNumber) {
            if (! preg_match('/^'.$escapedBase.'(?:-(\d+))?$/', $existingBatchNumber, $matches)) {
                continue;
            }

            if (! isset($matches[1])) {
                $baseAlreadyExists = true;

                continue;
            }

            $maxSuffix = max($maxSuffix, (int) $matches[1]);
        }

        if (! $baseAlreadyExists) {
            return $normalizedBatchNumber;
        }

        return $normalizedBatchNumber.'-'.($maxSuffix + 1);
    }

    private function normalizeBatchNumber(?string $batchNumber): ?string
    {
        $normalized = trim((string) $batchNumber);

        if ($normalized === '') {
            return null;
        }

        return mb_strtoupper($normalized);
    }
}
